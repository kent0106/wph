            <div class="intro"><h3>币类使用及余额说明</h3>
                <br>
                拍币，微排行用于竞拍的虚拟货币，每1拍币价值1元，每次出价会消耗一定数量的拍币。拍币可用于所有商品竞拍，不支持差价购买。
                拍币余额及收支明细，可进入“个人中心”—“拍币”中查询。
                <br>
                <br>
                赠币，即非人民币充值所获，有使用时间，可用于特惠商城中的实物竞拍。
                <br>
                1.如何获取赠币？
                <br>
                通常为活动赠送奖励，例如新手福利。
                <br>
                2.如何使用赠币？
                <br>
                赠币在有效期内可用于实物竞拍，不支持虚拟商品竞拍及差价购买，如竞拍失败不予退还。
                <br><br>
                购物币，为竞拍失败系统自动返还所消耗拍币的100%。购物币仅可用于差价购买，不支持任何商品竞拍。
                <br>
                1、全品类的购物币有效期至2018-12-31；
                <br>
                2、单个具体商品的购物币有效期为30天（从获得购物币的时间算起，30天后失效），账户中的购物币有效期届满后将会过期，
                过期后不可使用不可恢复。<br>
