            <div class="intro"><h3>币类使用及余额说明</h3>
                <br>
                拍币，微拍行用于竞拍的虚拟货币，每1拍币价值1元，每次出价会消耗一定数量的拍币。拍币可用于所有商品竞拍，不支持差价购买。
                拍币余额及收支明细，可进入“个人中心”—“拍币”中查询。
                <br>
                <br>
                赠币，即非人民币充值所获，有使用时间，可用于特惠商城中的实物竞拍。
                <br>
                1.如何获取赠币？
                <br>
                通常为活动赠送奖励，例如新手福利。
                <br>
                2.如何使用赠币？
                <br>
                赠币在有效期内可用于实物竞拍，不支持虚拟商品竞拍及差价购买，如竞拍失败不予退还。
                <br><br>
                购物币，为竞拍失败系统自动返还所消耗拍币的100%。购物币仅可用于差价购买，不支持任何商品竞拍。
                <br>
                3.如何获取推广币？
                <br>
                需要转发 “微拍行” 给微信好友【这是必要条件，如果好友是直接搜索微拍行进入的，您将不会有佣金抽成】，当邀请的微信好友充值，
                您将会返回相应的佣金3% 或 5% ，例如您邀请了小王，小王充值了100拍币，您将获得5元的佣金，若小王邀请了小张，小张充值
                了100拍币，则您会获取3元的佣金，小张获得5元的佣金。
